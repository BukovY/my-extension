function getArticleEdvance() {
    var array = [];
    var arrRez = [];
    var string = '<h3>Читайте также</h3><ul>';
    // получает заголовки статей
    $(".news__item").each(function() {
        array.push(this);
    });
    // вычлиняем ссылки статей
    for (var i of array) {
        arrRez.push([i.href])
    }
    // вычлиняем заголовки статей
    array = [];
    $(".news__item-title").each(function() {
        array.push(this);
    });
    // добавляем для отрисовки
    for (var i = 0; i < 5; i++) {
        string += '<li><a href="' + arrRez[i][0] + '">' + array[i].innerText + '</a></li>'
    }
    string += '</ul>'
    return string;
}

function getArticlePromtex() {
    var array = [];
    var arrRez = [];
    var arrLink = [0];
    var string = '<h3>Читайте также</h3><ul>';
    // получает ссылки статей
    jQuery(".tilink").each(function() {
        array.push(this);
    });
    // вычлиняем ссылки статей
    for (var i of array) {
        arrLink.push([i.href])
    }
    // удалим захваченный пустой элемент в ссылках
    arrLink = arrLink.filter(function(el) {
        return el != '0';
    });
    // вычлиняем заголовки статей
    array = [];
    // вычлиняем заголовки
    jQuery("h5").each(function() {
        array.push(this);
    });
    // удаляем переносы строк и лишние пробелы в начале и в конце строки
    for (var i of array) {
        arrRez.push([i.innerText.replace(/\r?\n/g, "").trim()])
    }
    // добавляем для отрисовки
    for (var i = 0; i < 5; i++) {
        string += '<li><a href="' + arrLink[i][0] + '">' + arrRez[i] + '</a></li>'
    }
    string += '</ul>'
    return string;
}

